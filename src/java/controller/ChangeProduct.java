    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //processRequest(request, response);
      String id= request.getParameter("id");
      DAO d= new DAO();
      String name =request.getParameter("name");
       String primaryPrice =request.getParameter("primaryPrice");
       String sale =request.getParameter("sale");
       String quantity_remain =request.getParameter("quantity_remain");
      String description =request.getParameter("description");
      String image=request.getParameter("image1");
      if(image.isEmpty()==false&&image.equals("null")==false&&request.getParameter("image1")!=null){
          d.addImageProduct(new ProductImage(id, 3, "images/"+image));
      }
      d.updateProduct(id, name, primaryPrice, sale, quantity_remain, description);
      request.getRequestDispatcher("EditProduct.jsp?id="+id).forward(request, response);
    }

