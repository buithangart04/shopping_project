    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
       String search = request.getParameter("search");
       DAO d= new DAO();
       List<Product> list= new ArrayList<>();
       list=d.searchTextProduct(search);
       if(search.isEmpty()|| request.getParameter("search")==null||search.equals("null")){
           request.getSession().setAttribute("error", "true");
       }
        int start=0,end=4;
         if(request.getParameter("start")!=null){
            start=Integer.parseInt(request.getParameter("start"));
        }if(request.getParameter("end")!=null){
            end=Integer.parseInt(request.getParameter("end"));
        }
        if(end>list.size()) end=list.size();
        request.getSession().setAttribute("listSearchpro", d.getProductByPage(list, start, end));
             request.setAttribute("size", list.size());
        request.getRequestDispatcher("SearchProduct.jsp?search="+search).forward(request, response);
    }

