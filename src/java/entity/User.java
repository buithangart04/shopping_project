package entity;

/**
 *
 * @author Admin
 */
public class User {
   String id ,pass,email,location,dob;
   int phone ;

    public User() {
    }

    public User(String id, String pass, String email, String location, String dob, int phone) {
        this.id = id;
        this.pass = pass;
        this.email = email;
        this.location = location;
        this.dob = dob;
        this.phone = phone;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }

    public int getPhone() {
        return phone;
    }

    public void setPhone(int phone) {
        this.phone = phone;
    }
   
}
